package Java_Study1_1;

public class Java_Study1_1_2 {

	public static void main(String[] args) {
		//真偽値の出力
		System.out.println(true);
		System.out.println(false);
		System.out.println(6 + 2 == 5);
		System.out.println(6 + 2 != 5);
		//論理演算子
		System.out.println(true && false);
		System.out.println(true || false);
	}

}
